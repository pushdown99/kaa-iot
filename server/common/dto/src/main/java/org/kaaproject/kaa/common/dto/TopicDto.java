package org.kaaproject.kaa.common.dto;

import java.io.Serializable;

public class TopicDto implements Serializable, HasId, Comparable<TopicDto> {

  private static final long serialVersionUID = -8370253094089760728L;

  private String id;
  private String applicationId;
  private String name;
  private TopicTypeDto type;
  private String description;
  private String createdUsername;
  private long createdTime;
  private int secNum;

  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }

  public String getApplicationId() {
    return applicationId;
  }

  public void setApplicationId(String applicationId) {
    this.applicationId = applicationId;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public TopicTypeDto getType() {
    return type;
  }

  public void setType(TopicTypeDto type) {
    this.type = type;
  }

  public int getSecNum() {
    return secNum;
  }

  public void setSecNum(int secNum) {
    this.secNum = secNum;
  }

  public String getDescription() {
    return description;
  }

  public void setDescription(String description) {
    this.description = description;
  }

  public String getCreatedUsername() {
    return createdUsername;
  }

  public void setCreatedUsername(String createdUsername) {
    this.createdUsername = createdUsername;
  }

  public long getCreatedTime() {
    return createdTime;
  }

  public void setCreatedTime(long createdTime) {
    this.createdTime = createdTime;
  }

  @Override
  public int hashCode() {
    final int prime = 31;
    int result = 1;
    result = prime * result
        + ((applicationId == null) ? 0 : applicationId.hashCode());
    result = prime * result + (int) (createdTime ^ (createdTime >>> 32));
    result = prime * result
        + ((createdUsername == null) ? 0 : createdUsername.hashCode());
    result = prime * result
        + ((description == null) ? 0 : description.hashCode());
    result = prime * result + ((id == null) ? 0 : id.hashCode());
    result = prime * result + ((name == null) ? 0 : name.hashCode());
    result = prime * result + secNum;
    result = prime * result + ((type == null) ? 0 : type.hashCode());
    return result;
  }

  @Override
  public boolean equals(Object obj) {
    if (this == obj) {
      return true;
    }
    if (obj == null) {
      return false;
    }
    if (getClass() != obj.getClass()) {
      return false;
    }
    TopicDto other = (TopicDto) obj;
    if (applicationId == null) {
      if (other.applicationId != null) {
        return false;
      }
    } else if (!applicationId.equals(other.applicationId)) {
      return false;
    }
    if (createdTime != other.createdTime) {
      return false;
    }
    if (createdUsername == null) {
      if (other.createdUsername != null) {
        return false;
      }
    } else if (!createdUsername.equals(other.createdUsername)) {
      return false;
    }
    if (description == null) {
      if (other.description != null) {
        return false;
      }
    } else if (!description.equals(other.description)) {
      return false;
    }
    if (id == null) {
      if (other.id != null) {
        return false;
      }
    } else if (!id.equals(other.id)) {
      return false;
    }
    if (name == null) {
      if (other.name != null) {
        return false;
      }
    } else if (!name.equals(other.name)) {
      return false;
    }
    if (secNum != other.secNum) {
      return false;
    }
    if (type != other.type) {
      return false;
    }
    return true;
  }

  @Override
  public String toString() {
    return "TopicDto [id=" + id + ", applicationId=" + applicationId
        + ", name=" + name + ", type=" + type + ", description="
        + description + ", createdUsername=" + createdUsername
        + ", createdTime=" + createdTime + ", secNum=" + secNum + "]";
  }

  @Override
  public int compareTo(TopicDto topicDto) {
    return Long.compare(Long.parseLong(this.id), Long.parseLong(topicDto.id));
  }

}
